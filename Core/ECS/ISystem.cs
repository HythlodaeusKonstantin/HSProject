namespace Engine.Core.ECS
{
    /// <summary>
    /// Базовый интерфейс для всех систем ECS.
    /// Система получает дельта-время и выполняет свою логику обновления.
    /// </summary>
    public interface ISystem
    {
        /// <summary>
        /// Вызывается каждый кадр с накопленным дельта-временем.
        /// </summary>
        /// <param name="deltaTime">Время (в секундах) с момента предыдущего вызова.</param>
        void Update(double deltaTime);

        /// <summary>
        /// Вызывается для отрисовки системы (опционально).
        /// Реализуется только системами, которые требуют рендеринга.
        /// </summary>
        void Render();
    }
} 